// Step 1: Login to GitHub → Click 'New' → Create repository (Public)
// Step 2: Repository name: js-day1-assignment
// Step 3: Click "Code" → Select "Codespaces" → "Create codespace on main"
// Step 4: Codespace (online VS Code) will open in browser
// Step 5: In Codespaces terminal, check Node: node -v
// Step 6: Create files from Explorer → intro.js, setup.js, README.md
// Step 7: Run JS file using terminal command: node intro.js
// Step 8: Stage changes using Source Control tab
// Step 9: Write commit message → Click Commit
// Step 10: Click "Sync Changes" → Push code to GitHub
